// Write a JavaScript code snippet that uses arithmetic, comparison, and logical operators to determine if a number is both even and greater than 10. Display the result using console.log.

let number = 12;

if (number % 2 === 0 && number > 10) {
  console.log("The number is both even and greater than 10.");
} else {
  console.log("The number is either not even or not greater than 10.");
}
